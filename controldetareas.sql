	END sp_usuario_actualizar;
	
	PROCEDURE sp_usuario_eliminar(p_id NUMBER) IS
	BEGIN
		DELETE FROM auth_user
		WHERE id = p_id;
	END sp_usuario_eliminar;

	PROCEDURE sp_usuario_login(p_email VARCHAR2, p_password VARCHAR2, registros OUT SYS_REFCURSOR) IS
	BEGIN
		OPEN registros FOR
		SELECT id, f.id_rol, a.first_name, r.nombre FROM auth_user a
    INNER JOIN funcionario f ON a.id = f.user_id
    INNER JOIN rol_funcionario r ON f.id_rol = r.id_rol
		WHERE a.email = p_email AND a.password = p_password;
	END sp_usuario_login;
END pkg_usuario;

